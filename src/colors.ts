export function index2bgColor(index: number) {
  switch (index) {
    case 0:
      return 'bg-red-500'
    case 1:
      return 'bg-green-500'
    case 2:
      return 'bg-blue-500'
    case 3:
      return 'bg-yellow-500'
    case 4:
      return 'bg-pink-500'
    case 5:
      return 'bg-purple-500'
  }
}
